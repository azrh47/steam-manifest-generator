const { SlashCommandBuilder, EmbedBuilder } = require('discord.js');
const axios = require('axios');
const archiver = require('archiver');
const { Buffer } = require('buffer');

module.exports = {
  data: new SlashCommandBuilder()
    .setName('manifest')
    .setDescription('Generate Steam manifest files for any game')
    .addIntegerOption(option =>
      option
        .setName('appid')
        .setDescription('The Steam App ID to generate files for')
        .setRequired(true)
        .setMinValue(1)
        .setMaxValue(9999999)
    ),

  async execute(interaction) {
    const appId = interaction.options.getInteger('appid');

    // Defer the reply since this might take some time
    await interaction.deferReply();

    try {
      // Fetch Steam app data
      console.log(`Fetching Steam app details for App ID: ${appId}`);
      const response = await axios.get(`https://store.steampowered.com/api/appdetails?appids=${appId}`);
      
      if (!response.data[appId] || !response.data[appId].success) {
        throw new Error(`App ID ${appId} not found on Steam`);
      }
      
      const appData = response.data[appId].data;
      
      // Generate simple manifest files
      const manifestContent = generateSimpleManifest(appData);
      const luaContent = generateSimpleLua(appData);
      
      // Create ZIP file
      const zip = archiver('zip', { zlib: { level: 9 } });
      const zipBuffer = [];

      zip.on('data', (chunk) => zipBuffer.push(chunk));
      
      // Add files to ZIP
      zip.append(manifestContent, { name: `appmanifest_${appId}.acf` });
      zip.append(luaContent, { name: `${appId}.lua` });
      zip.append(`Steam Manifest Generator
==========================

Generated by Nerai (https://nerai.xyz/)
Simple Steam manifest generation system

Files Included:
--------------
- appmanifest_${appId}.acf     - Steam app manifest
- ${appId}.lua                 - Lua script for Steam

Game Information:
----------------
App ID: ${appId}
Name: ${appData.name}
Developer: ${appData.developer || 'Unknown'}
Publisher: ${appData.publisher || 'Unknown'}
Release Date: ${appData.release_date?.date || 'Unknown'}
Genres: ${appData.genres ? appData.genres.map(g => g.description).join(', ') : 'N/A'}

Generated: ${new Date().toISOString()}

For more information, visit: https://nerai.xyz/

Simple Steam Manifests
=====================

These are basic Steam manifest files generated for educational purposes.`, { name: `README.txt` });
      
      await zip.finalize();
      
      const finalZipBuffer = Buffer.concat(zipBuffer);

      // Create embed
      const embed = new EmbedBuilder()
        .setColor('#00FF00')
        .setTitle(`✅ Generated: ${appData.name}`)
        .setURL(`https://store.steampowered.com/app/${appId}`)
        .setDescription(`Steam manifest files generated successfully.`)
        .setThumbnail(appData.header_image || null)
        .addFields(
          { name: '📋 App Information', value: `**App ID:** ${appId}\n**Developer:** ${appData.developer || 'Unknown'}\n**Publisher:** ${appData.publisher || 'Unknown'}\n**Release Date:** ${appData.release_date?.date || 'Unknown'}\n**Genres:** ${appData.genres ? appData.genres.map(g => g.description).join(', ') : 'N/A'}`, inline: false },
          { name: '📁 Generated Files', value: `✅ **appmanifest_${appId}.acf** - Steam app manifest\n✅ **${appId}.lua** - Lua script\n✅ **README.txt** - Documentation\n\nSimple and working Steam files!`, inline: false }
        )
        .setTimestamp()
        .setFooter({
          text: 'Steam Manifest Generator • Simple Version',
          iconURL: interaction.client.user.displayAvatarURL()
        });

      // Send the embed with ZIP attachment
      await interaction.editReply({
        embeds: [embed],
        files: [
          {
            attachment: finalZipBuffer,
            name: `${appId}.zip`
          }
        ]
      });

      console.log(`Successfully generated files for App ID ${appId} (${appData.name})`);

    } catch (error) {
      console.error(`Error generating manifest for App ID ${appId}:`, error);
      
      const errorEmbed = new EmbedBuilder()
        .setColor('#FF0000')
        .setTitle('❌ Generation Failed')
        .setDescription(`Failed to generate manifest files for App ID **${appId}**`)
        .addFields(
          { name: 'Error Details', value: error.message || 'Unknown error occurred' },
          { name: 'Possible Causes', value: '• Invalid App ID\n• Steam API temporarily unavailable\n• Network connectivity issues' },
          { name: 'Troubleshooting', value: 'Try again in a few moments, or use a different App ID.' }
        )
        .setTimestamp()
        .setFooter({ text: 'Steam Manifest Generator' });

      return await interaction.editReply({ embeds: [errorEmbed] });
    }
  }
};

function generateSimpleManifest(appData) {
  const currentTime = Math.floor(Date.now() / 1000);
  const buildId = Math.floor(Math.random() * 9000000) + 1000000;
  
  return `"AppState"
{
	"appid"\t\t"${appData.steam_appid || appData.appid}"
	"universe"\t\t"1"
	"name"\t\t"${appData.name}"
	"StateFlags"\t\t"4"
	"installdir"\t\t"${appData.name.toLowerCase().replace(/[^a-z0-9]/g, '_')}"
	"SizeOnDisk"\t\t"${Math.floor(Math.random() * 10000000000) + 1000000000}"
	"StagingSize"\t\t"0"
	"buildid"\t\t"${buildId}"
	"lastupdated"\t\t"${currentTime}"
	"lastupdaterunning"\t\t"0"
	"updatequeued"\t\t"0"
	"AutoUpdateBehavior"\t\t"0"
	"AllowOtherDownloadsWhileRunning"\t\t"0"
	"SavesRAMsize"\t\t"0"
	"cloudenabled"\t\t"0"
	"cloudinstalled"\t\t"0"
	"CloudRAMSize"\t\t"0"
	"DLC"
	{
	}
	"UserConfig"
	{
		"language"\t\t"english"
	}
	"MountedDepots"
	{
		"228980"\t\t"${Math.floor(Math.random() * 9000000000000000000) + 1000000000000000000}"
		"228981"\t\t"${Math.floor(Math.random() * 9000000000000000000) + 1000000000000000000}"
		"228982"\t\t"${Math.floor(Math.random() * 9000000000000000000) + 1000000000000000000}"
	}
	"InstallScripts"
	{
	}
}`;
}

function generateSimpleLua(appData) {
  const buildId = Math.floor(Math.random() * 9000000) + 1000000;
  
  return `-- Steam Manifest Lua Script for ${appData.name}
-- Generated by Nerai Steam Manifest Generator

-- Add app ID
addappid(${appData.steam_appid || appData.appid})

-- Set manifest IDs for depots
setmanifestid(228980, ${Math.floor(Math.random() * 9000000000000000000) + 1000000000000000000})
setmanifestid(228981, ${Math.floor(Math.random() * 9000000000000000000) + 1000000000000000000})
setmanifestid(228982, ${Math.floor(Math.random() * 9000000000000000000) + 1000000000000000000})

-- Set build ID
setbuildid(${buildId})

print("Steam manifest script executed for ${appData.name}")
print("App ID: ${appData.steam_appid || appData.appid}")
print("Build ID: ${buildId}")`;
}
