const { SlashCommandBuilder, EmbedBuilder } = require('discord.js');
const axios = require('axios');
const archiver = require('archiver');
const { Buffer } = require('buffer');

module.exports = {
  data: new SlashCommandBuilder()
    .setName('manifest')
    .setDescription('Generate Steam manifest files using real Steam data')
    .addIntegerOption(option =>
      option
        .setName('appid')
        .setDescription('The Steam App ID to generate files for')
        .setRequired(true)
        .setMinValue(1)
        .setMaxValue(9999999)
    ),

  async execute(interaction) {
    const appId = interaction.options.getInteger('appid');

    // Defer the reply since this might take some time
    await interaction.deferReply();

    try {
      // Fetch Steam app data
      console.log(`Fetching Steam app details for App ID: ${appId}`);
      const response = await axios.get(`https://store.steampowered.com/api/appdetails?appids=${appId}`);
      
      if (!response.data[appId] || !response.data[appId].success) {
        throw new Error(`App ID ${appId} not found on Steam`);
      }
      
      const appData = response.data[appId].data;
      
      // Generate realistic manifest files based on real Steam data
      const manifestContent = generateRealisticManifest(appData, appId);
      const luaContent = generateRealisticLua(appData, appId);
      
      // Create ZIP file
      const zip = archiver('zip', { zlib: { level: 9 } });
      const zipBuffer = [];

      zip.on('data', (chunk) => zipBuffer.push(chunk));
      
      // Add files to ZIP
      zip.append(manifestContent, { name: `appmanifest_${appId}.acf` });
      zip.append(luaContent, { name: `${appId}.lua` });
      zip.append(`Steam Manifest Generator
==========================

Generated by Nerai (https://nerai.xyz/)
Realistic Steam manifest generation system

Files Included:
--------------
- appmanifest_${appId}.acf     - Steam app manifest
- ${appId}.lua                 - Lua script for Steam

Game Information:
----------------
App ID: ${appId}
Name: ${appData.name}
Developer: ${appData.developer || 'Unknown'}
Publisher: ${appData.publisher || 'Unknown'}
Release Date: ${appData.release_date?.date || 'Unknown'}
Genres: ${appData.genres ? appData.genres.map(g => g.description).join(', ') : 'N/A'}
Price: ${appData.price_overview ? '$' + (appData.price_overview.final / 100).toFixed(2) : 'Free'}
Metacritic: ${appData.metacritic ? appData.metacritic.score + '/100' : 'N/A'}

Generated: ${new Date().toISOString()}

Realistic Steam Manifests
=========================

These manifest files are generated using real Steam game data
to create realistic and educational Steam manifest examples.

For more information, visit: https://nerai.xyz/`, { name: `README.txt` });
      
      await zip.finalize();
      
      const finalZipBuffer = Buffer.concat(zipBuffer);

      // Create embed
      const embed = new EmbedBuilder()
        .setColor('#00FF00')
        .setTitle(`✅ Generated: ${appData.name}`)
        .setURL(`https://store.steampowered.com/app/${appId}`)
        .setDescription(`Steam manifest files generated using real Steam data.`)
        .setThumbnail(appData.header_image || null)
        .addFields(
          { name: '📋 App Information', value: `**App ID:** ${appId}\n**Developer:** ${appData.developer || 'Unknown'}\n**Publisher:** ${appData.publisher || 'Unknown'}\n**Release Date:** ${appData.release_date?.date || 'Unknown'}\n**Genres:** ${appData.genres ? appData.genres.map(g => g.description).join(', ') : 'N/A'}\n**Price:** ${appData.price_overview ? '$' + (appData.price_overview.final / 100).toFixed(2) : 'Free'}`, inline: false },
          { name: '📁 Generated Files', value: `✅ **appmanifest_${appId}.acf** - Steam app manifest\n✅ **${appId}.lua** - Lua script\n✅ **README.txt** - Documentation\n\nRealistic Steam files based on real data!`, inline: false }
        )
        .setTimestamp()
        .setFooter({
          text: 'Steam Manifest Generator • Realistic Version',
          iconURL: interaction.client.user.displayAvatarURL()
        });

      // Send the embed with ZIP attachment
      await interaction.editReply({
        embeds: [embed],
        files: [
          {
            attachment: finalZipBuffer,
            name: `${appId}.zip`
          }
        ]
      });

      console.log(`Successfully generated files for App ID ${appId} (${appData.name})`);

    } catch (error) {
      console.error(`Error generating manifest for App ID ${appId}:`, error);
      
      const errorEmbed = new EmbedBuilder()
        .setColor('#FF0000')
        .setTitle('❌ Generation Failed')
        .setDescription(`Failed to generate manifest files for App ID **${appId}**`)
        .addFields(
          { name: 'Error Details', value: error.message || 'Unknown error occurred' },
          { name: 'Possible Causes', value: '• Invalid App ID\n• Steam API temporarily unavailable\n• Network connectivity issues' },
          { name: 'Troubleshooting', value: 'Try again in a few moments, or use a different App ID.' }
        )
        .setTimestamp()
        .setFooter({ text: 'Steam Manifest Generator' });

      return await interaction.editReply({ embeds: [errorEmbed] });
    }
  }
};

function generateRealisticManifest(appData, appId) {
  const currentTime = Math.floor(Date.now() / 1000);
  
  // Generate realistic build ID based on app data
  const baseBuildId = appId * 10000 + Math.floor(Date.now() / 100000);
  const buildId = baseBuildId + 1000000;
  
  // Estimate realistic file size based on game data
  let estimatedSize = 1000000000; // 1GB base
  
  if (appData.genres) {
    const genres = appData.genres.map(g => g.description.toLowerCase());
    if (genres.includes('action') || genres.includes('adventure')) {
      estimatedSize *= 3; // Action games are larger
    }
    if (genres.includes('rpg')) {
      estimatedSize *= 4; // RPGs are very large
    }
    if (genres.includes('indie')) {
      estimatedSize *= 0.5; // Indie games are smaller
    }
    if (genres.includes('simulation')) {
      estimatedSize *= 2; // Simulation games are medium-large
    }
  }
  
  // Adjust based on price (more expensive games tend to be larger)
  if (appData.price_overview && appData.price_overview.final > 2000) {
    estimatedSize *= 1.5;
  }
  
  // Generate realistic manifest IDs based on app ID
  const generateManifestId = (depotId) => {
    return (appId * 1000000000000 + depotId * 100000000 + currentTime).toString();
  };
  
  return `"AppState"
{
	"appid"\t\t"${appId}"
	"universe"\t\t"1"
	"name"\t\t"${appData.name}"
	"StateFlags"\t\t"4"
	"installdir"\t\t"${appData.name.toLowerCase().replace(/[^a-z0-9]/g, '_')}"
	"SizeOnDisk"\t\t"${Math.floor(estimatedSize)}"
	"StagingSize"\t\t"0"
	"buildid"\t\t"${buildId}"
	"lastupdated"\t\t"${currentTime}"
	"lastupdaterunning"\t\t"0"
	"updatequeued"\t\t"0"
	"AutoUpdateBehavior"\t\t"0"
	"AllowOtherDownloadsWhileRunning"\t\t"0"
	"SavesRAMsize"\t\t"0"
	"cloudenabled"\t\t"0"
	"cloudinstalled"\t\t"0"
	"CloudRAMSize"\t\t"0"
	"DLC"
	{
	}
	"UserConfig"
	{
		"language"\t\t"english"
	}
	"MountedDepots"
	{
		"228980"\t\t"${generateManifestId(228980)}"
		"228981"\t\t"${generateManifestId(228981)}"
		"228982"\t\t"${generateManifestId(228982)}"
	}
	"InstallScripts"
	{
	}
}`;
}

function generateRealisticLua(appData, appId) {
  const baseBuildId = appId * 10000 + Math.floor(Date.now() / 100000);
  const buildId = baseBuildId + 1000000;
  const currentTime = Math.floor(Date.now() / 1000);
  
  // Generate realistic manifest IDs
  const generateManifestId = (depotId) => {
    return (appId * 1000000000000 + depotId * 100000000 + currentTime).toString();
  };
  
  return `-- Steam Manifest Lua Script for ${appData.name}
-- Generated by Nerai Steam Manifest Generator
-- Based on real Steam data: App ID ${appId}, Release Date: ${appData.release_date?.date || 'Unknown'}

-- Add app ID to Steam
addappid(${appId})

-- Set realistic manifest IDs for standard depots
setmanifestid(228980, ${generateManifestId(228980)})
setmanifestid(228981, ${generateManifestId(228981)})
setmanifestid(228982, ${generateManifestId(228982)})

-- Set build ID based on app data
setbuildid(${buildId})

-- Game information
print("Steam manifest script executed for: ${appData.name}")
print("App ID: ${appId}")
print("Build ID: ${buildId}")
print("Developer: ${appData.developer || 'Unknown'}")
print("Release Date: ${appData.release_date?.date || 'Unknown'}")
${appData.genres ? `print("Genres: ${appData.genres.map(g => g.description).join(', ')}")` : ''}
${appData.price_overview ? `print("Price: $${(appData.price_overview.final / 100).toFixed(2)}")` : 'print("Price: Free")'}

-- Script completed successfully
print("Steam manifest generation completed!")`;
}
